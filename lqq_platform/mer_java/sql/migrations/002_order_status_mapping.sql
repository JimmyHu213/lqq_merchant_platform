-- ┌─────────────────────────────────────────────────────────────────┐
-- │              mlqApi 订单状态 → JAVA-MER 订单状态映射             │
-- ├────────┬──────────────┬────────┬──────────────┬─────────────────┤
-- │ mlqApi │ mlqApi 含义   │ MER    │ MER 含义      │ 补充说明         │
-- ├────────┼──────────────┼────────┼──────────────┼─────────────────┤
-- │ 101    │ 已下单未支付   │ 0      │ 待支付        │ 直接映射          │
-- │ 102    │ 用户取消(未付) │ 9      │ 已取消        │ cancel_status=2  │
-- │ 103    │ 系统取消(超时) │ 9      │ 已取消        │ cancel_status=1  │
-- │ 201    │ 已支付未发货   │ 1      │ 待发货        │ paid=1           │
-- │ 202    │ 已支付-退款中  │ 1      │ 待发货        │ refund_status=1  │
-- │ 203    │ 退款已确认     │ 9      │ 已取消        │ refund_status=3  │
-- │ 301    │ 已发货        │ 4      │ 待收货        │ 线上物流场景      │
-- │ 401    │ 确认收货      │ 5      │ 已收货        │ 用户手动确认      │
-- │ 402    │ 系统自动收货   │ 5      │ 已收货        │ 系统自动确认      │
-- └────────┴──────────────┴────────┴──────────────┴─────────────────┘

-- ┌─────────────────────────────────────────────────────────────────┐
-- │              JAVA-MER 完整订单状态参考                           │
-- ├────────┬──────────────┬─────────────────────────────────────────┤
-- │ 状态码  │ 含义          │ 常量名                                 │
-- ├────────┼──────────────┼─────────────────────────────────────────┤
-- │ 0      │ 待支付        │ ORDER_STATUS_WAIT_PAY                  │
-- │ 1      │ 待发货        │ ORDER_STATUS_WAIT_SHIPPING             │
-- │ 2      │ 部分发货      │ ORDER_STATUS_PART_SHIPPING             │
-- │ 3      │ 待核销        │ ORDER_STATUS_AWAIT_VERIFICATION        │
-- │ 4      │ 待收货        │ ORDER_STATUS_WAIT_RECEIPT              │
-- │ 5      │ 已收货        │ ORDER_STATUS_TAKE_DELIVERY             │
-- │ 6      │ 已完成        │ ORDER_STATUS_COMPLETE                  │
-- │ 9      │ 已取消        │ ORDER_STATUS_CANCEL                    │
-- └────────┴──────────────┴─────────────────────────────────────────┘

-- 注意:
-- 1. JAVA-MER 有「待核销」状态(3)，可直接用于溜圈圈的线下核销场景 (B5模块)
-- 2. JAVA-MER 有独立的 refund_status 和 cancel_status 字段，比 mlqApi 更精细
-- 3. 迁移数据时需根据 mlqApi 的 orderStatus 同时设置 MER 的 status + cancel_status + refund_status
-- 4. mlqApi 状态 201(已支付) 在 MER 中对应 status=1 + paid=1 + pay_time=原支付时间
